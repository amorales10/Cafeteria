    private void jButton_EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EditarActionPerformed
        // TODO add your handling code here:
        Boolean condicion;

        condicion = jTextField_Nombre.getText().equals("") || jTextField_Referencia.getText().equals("") || jTextField_Precio.getText().equals("") || jTextField_Peso.getText().equals("") || jTextField_Cantidad.getText().equals("");
        condicion = condicion || jComboBox_Categoria.getSelectedIndex() == 0;
        if (condicion) {
            JOptionPane.showMessageDialog(null, "Campos vacios", "INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE);
        } else {

            try {
                bd.ConectarBasedeDatos();

                String nombre, referencia, prec, pes, cantidad, cod;
                int precio, peso, stock, categoria, id;
                BD.Categorias cb_cate = (BD.Categorias) this.jComboBox_Categoria.getSelectedItem();

                nombre = this.jTextField_Nombre.getText();
                referencia = this.jTextField_Referencia.getText();
                prec = this.jTextField_Precio.getText();
                precio = Integer.parseInt(prec);
                pes = this.jTextField_Peso.getText();
                peso = Integer.parseInt(pes);
                categoria = cb_cate.getId_cate();
                cantidad = this.jTextField_Cantidad.getText();
                stock = Integer.parseInt(cantidad);
                LocalDateTime fecha_de_creacion = LocalDateTime.now();
                cod = this.jTextField_ID.getText();
                id = Integer.parseInt(cod);

                bd.sentencia.execute("UPDATE productos	SET nombre_prod='" + nombre + "', referencia_prod='" + referencia + "', precio_prod='" + precio + "', peso_prod='" + peso + "', stock_prod='" + stock + "', fecha_creacion_prod='" + fecha_de_creacion + "', id_cate='" + categoria + "' WHERE  id_prod='" + id + "'");

                JOptionPane.showMessageDialog(null, "El Producto ha sido actualizado correctamente", "Información", JOptionPane.INFORMATION_MESSAGE);
                jTextField_Nombre.setText("");
                jTextField_Referencia.setText("");
                jTextField_Precio.setText("");
                jTextField_Peso.setText("");
                jTextField_Cantidad.setText("");
                jComboBox_Categoria.setSelectedIndex(0);
                jTextField_ID.setText("");

                bd.DesconectarBasedeDatos();

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }

        }
        jButton_Cancelar.setVisible(false);

    }//GEN-LAST:event_jButton_EditarActionPerformed

