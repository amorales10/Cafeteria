    private void jTable_Lista_ProductosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable_Lista_ProductosMouseClicked
        // TODO add your handling code here:

        jButton_Guardar.setEnabled(false);
        jButton_Editar.setEnabled(true);
        jButton_Eliminar.setEnabled(true);
        jButton_Cancelar.setVisible(true);
        Desbloquear();
        if (evt.getButton() == 1) {
            PreparedStatement ps = null;
            ResultSet rs = null;
            Connection con = bd.Conectando();
            try {

                int fila = jTable_Lista_Productos.getSelectedRow();
                String sql = "Select id_prod, nombre_prod, referencia_prod, precio_prod, peso_prod, stock_prod, id_cate from productos Where id_prod=" + jTable_Lista_Productos.getValueAt(fila, 0);
                ps = con.prepareStatement(sql);
                rs = ps.executeQuery();
                rs.next();
                jTextField_ID.setText(rs.getString("id_prod"));
                jTextField_Nombre.setText(rs.getString("nombre_prod"));
                jTextField_Referencia.setText(rs.getString("referencia_prod"));
                jTextField_Precio.setText(rs.getString("precio_prod"));
                jTextField_Peso.setText(rs.getString("peso_prod"));
                jTextField_Cantidad.setText(rs.getString("stock_prod"));
                jTextField_Referencia.setText(rs.getString("referencia_prod"));
                jComboBox_Categoria.setSelectedIndex(rs.getInt("id_cate"));

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jTable_Lista_ProductosMouseClicked

